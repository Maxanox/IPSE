	<h1 class="h1">I.P.S.E Welcomes You</h1>
	<p><u><strong>Interactive Physique Simulation Engine Project</strong></u></p>
	<section>
		<p><strong>
			The project aims to develop a physics simulation engine with an interactive graphical interface using Rust. The objective is to allow users to explore and manipulate various scenarios of physics simulation, ranging from intuitive and playful to more complex and faithful to reality.

This project appears particularly relevant for leveraging Rust's capabilities and performance, given the requirement to perform complex scientific calculations. Physics simulation often demands a high number of calculations per second, necessitating rigorous optimization of algorithms.

The "Interactive" aspect of the project stems from how the software will be utilized. The physics and objects to which it is applied are already available: balls, blocks, other solids, particles, fluids, solid cannons (which continuously project a certain solid), fluid cannons (similarly with a certain fluid or particle), magnetic/gravitational attraction points, cables, anchor points (to attach other objects, allowing the creation of pulleys, pendulums, etc.).

Users will have the choice: either they launch pre-made templates provided by us and observe the outcomes while being able to modify certain parameters (bonus if the rest of the project progresses well), or they have a blank space where they can drag and drop and arrange interactive physical objects as they wish, modify parameters, and launch the simulation to see the results.
			</strong>
		</p>
